
public class Employee {

	private int hours;
	private double salary;
	
	//constructor
	public Employee(){
		hours=0;
		salary=0;
	}
		
	//methods
	public int getHours(){
		return hours;
	}
	
	public double getSalary(){
		return salary;
	}
	
	public void setHours(int myHours){
		hours=myHours;
	}
	public void setSalary(double mySalary){
		salary=mySalary;
	}
	
	

}




